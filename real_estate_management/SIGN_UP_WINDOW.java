    private void jButton_SignUP_SignUPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SignUP_SignUPActionPerformed
        // TODO add your handling code here:
        PreparedStatement ps;
        ResultSet rs;
        String userType = jComboBox_SignUP_UserType.getSelectedItem().toString();
        String name = jTextField_SignUP_Name.getText();
        String DOB = jFormattedTextField_SignUP_DOB.getText();
        String phone = jTextField_SignUP_Phone.getText();
        String email = jTextField_SignUP_Email.getText();
        String password = jTextField_SignUP_Password.getText();
        String address = jTextArea_SignUP_Address.getText();
        String insertQuery = "Insert into Buyer(name, DOB, phone, address, email, password) value (?, ?, ?, ?, ?, ?);";
        if(userType.equals("Seller")){
            insertQuery="Insert into Seller(name, DOB, phone, address, email, password) value (?, ?, ?, ?, ?, ?);";
        }
        try{
            ps = THE_CONNECTION.getTheConnection().prepareStatement(insertQuery);
            //ps.setString(1, userType);
            ps.setString(1, name);
            ps.setString(2, DOB);
            ps.setString(3, phone);
            ps.setString(4, address);
            ps.setString(5, email);
            ps.setString(6, password);
            //ps.executeUpdate();

            if (ps.executeUpdate() != 0){
                LOGIN w4 = new LOGIN();
                w4.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"INVALID DETAILS");
            }
        } catch (SQLException ex) {
            Logger.getLogger(SELLER_LOGIN_WINDOW.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null,"INVALID DETAILS");
        }


        
    }//GEN-LAST:event_jButton_SignUP_SignUPActionPerformed
